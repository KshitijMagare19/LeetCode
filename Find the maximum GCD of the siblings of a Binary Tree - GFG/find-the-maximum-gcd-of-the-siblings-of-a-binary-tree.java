class Solution {
    int[] ans = new int[]{0, 0};//{gcd, node}
    int maxGCD(Node root) {
        if(root == null) return -1;
        ans = new int[]{0, 0};
        helper(root);
        return ans[1];
    }
    
    Node helper(Node node){
        if(node == null) return null;
        
        Node left = helper(node.left);
        Node right = helper(node.right);
        
        if(left != null && right != null){
            int min = left.data < right.data ? left.data : right.data;
            int max = left.data > right.data ? left.data : right.data;
            int currGCD = gcd(max, min);
            
            if(currGCD > ans[0]){
                ans[0] = currGCD;
                ans[1] = node.data;
            }
        }
        return node;
    }
    
    // recursion to calculate GCD
    int gcd(int a, int b){
        if(b == 0) return a;
        return gcd(b, a%b);
    }
}

